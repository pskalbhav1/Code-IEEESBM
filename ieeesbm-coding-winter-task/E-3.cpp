#include<iostream>
using namespace std;
int main()
{
  double a, b;
  cout << "Enter two Numbers :: ";
  cin >> a >> b;
  cout << a << "+" << b << " = "<< a+b << endl;
  cout << a << "-" << b << " = "<< a-b << endl;
  cout << a << "*" << b << " = "<< a*b << endl;
  cout << a << "/" << b << " = "<< a/b << endl;

  return 0;
}